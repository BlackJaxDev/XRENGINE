namespace XREngine;

/// <summary>
/// Represents the different shadelet density rates available for the RVC rendering system.
/// </summary>
public enum ERvcShadeletDensity
{
    /// <summary>
    /// 1x1 shadelet density rate.
    /// </summary>
    Rate1x1 = 1,
    /// <summary>
    /// 2x2 shadelet density rate.
    /// </summary>
    Rate2x2 = 2,
    /// <summary>
    /// 4x4 shadelet density rate.
    /// </summary>
    Rate4x4 = 4,
    /// <summary>
    /// 8x8 shadelet density rate.
    /// </summary>
    Rate8x8 = 8,
}
